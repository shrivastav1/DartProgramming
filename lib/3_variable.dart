// Varible in Dart
void main(){

  // Static Varible
  int _num;
  _num = 12;
  String $name = "sakshi";
  double my_number = 55;
  bool is_studnet = true;

  print(_num);
  print($name);
  print(my_number);
  print(is_studnet);

  // Dynamic Variable
    dynamic $title1 = "This is title";
    print($title1);
    $title1 = 3.14157;
    print($title1);

  //final keyword
    final student_name = "sakshi";
    print(student_name);

  //const keyword
  const String name1 = "Dharmik";   // const define same line
  print("the name is : $name1");

  const name2 = "Shrivastav";
  print(name2);

  //null variable
   String? a;
   a=null;
   print(a);
}


